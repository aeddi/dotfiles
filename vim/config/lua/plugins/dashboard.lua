local function generate_date_header()
  local weekdays = { "Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday" }
  local ascii_weekday = ascii_weekdays[weekdays[os.date("*t").wday]]
  local header_list = vim.list_extend(ascii_weekday, { os.date(" %d %B %Y    %H:%M"), "", "" })
  local header = ""
  for _, v in pairs(header_list) do
    header = header .. v .. "\n"
  end
  return header
end

return {
  "folke/snacks.nvim",
  opts = {
    dashboard = {
      preset = {
        header = generate_date_header(),
      },
    },
  },
}
